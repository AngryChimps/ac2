            }


            $request = $this->guzzle->createRequest('GET', $url, [
                'headers' => [
                    $this->sessionHeaderName => $this->sessionId,
                    'content-type' => 'application/json',
                ],
//                'exceptions' => false,
            ]);

            return $this->guzzle->send($request);
//        }
//        catch(\Exception $ex) {
